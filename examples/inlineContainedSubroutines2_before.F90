  P6=5.
  P6(:KSIZE2)=P6(:KSIZE2)
  P6(2:KSIZE2)=P6(2:KSIZE2)

  print*, P, Q

END SUBROUTINE INC1

SUBROUTINE INC2(Z1, Z2)
  REAL, DIMENSION(KSIZE2) :: Z1, Z2
  Z1(:)=Z2(:)
END SUBROUTINE INC2

SUBROUTINE INC3(P1, P2)
  REAL, OPTIONAL, DIMENSION(KSIZE2) :: P1, P2
